type SearchParams = Record<string, string | string[] | undefined> | undefined

export function resolveLocale(searchParams?: SearchParams): Locale {
  const value = searchParams?.lang
  const lang = Array.isArray(value) ? value[0] : value
  return locales.includes(lang as Locale) ? (lang as Locale) : "en"
}

export function withLocale(href: string, locale: Locale) {
  if (href.startsWith("http") || href.startsWith("mailto:") || href.startsWith("tel:")) {
    return href
  }
  const [pathWithHash, hash = ""] = href.split("#")
  const [path, query = ""] = pathWithHash.split("?")
  const params = new URLSearchParams(query)
  params.set("lang", locale)
  const queryString = params.toString()
  return `${path || "/"}${queryString ? `?${queryString}` : ""}${hash ? `#${hash}` : ""}`
}

